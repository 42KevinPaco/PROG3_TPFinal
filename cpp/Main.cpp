double convertirRadianes(double valor) {
    return valor * 3.14159265358979323846 / 180.0;
}

double calcularDistanciaCoordenadas(double latitud1, double longitud1,
                                    double latitud2, double longitud2) {
    double radioTierraKm = 6371.0;
    double dLat = convertirRadianes(latitud2 - latitud1);
    double dLon = convertirRadianes(longitud2 - longitud1);
    double lat1 = convertirRadianes(latitud1);
    double lat2 = convertirRadianes(latitud2);

    double a = sin(dLat / 2.0) * sin(dLat / 2.0) +
               cos(lat1) * cos(lat2) * sin(dLon / 2.0) * sin(dLon / 2.0);
    double c = 2.0 * atan2(sqrt(a), sqrt(1.0 - a));

    return radioTierraKm * c;
}

